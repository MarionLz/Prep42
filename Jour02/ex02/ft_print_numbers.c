#include <unistd.h>

int ft_putchar(char c)
{
  write(1, &c, 1);
  return (0);
}

void ft_print_numbers(void)
{
  char i;
  char c = '0';
    
  i = c;
  while (i <= '9')
    {
      ft_putchar(c);
      i = i +1;
      c = c +1;
    }
}

int main()
{
  ft_print_numbers();
  ft_putchar('\n');
  return (0);
}
